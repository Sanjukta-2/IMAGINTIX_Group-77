IMG_HEIGHT, IMG_WIDTH = 256, 256

def load_image(input_path, target_path):
    # Read input
    input_img = tf.io.read_file(input_path)
    input_img = tf.image.decode_image(input_img, channels=3)
    input_img.set_shape([None, None, 3])  # <--- FIX: explicitly set shape
    input_img = tf.image.convert_image_dtype(input_img, tf.float32)
    input_img = tf.image.resize(input_img, [IMG_HEIGHT, IMG_WIDTH])

    # Read target
    target_img = tf.io.read_file(target_path)
    target_img = tf.image.decode_image(target_img, channels=3)
    target_img.set_shape([None, None, 3])  # <--- FIX: explicitly set shape
    target_img = tf.image.convert_image_dtype(target_img, tf.float32)
    target_img = tf.image.resize(target_img, [IMG_HEIGHT, IMG_WIDTH])

    # Normalize to [-1,1]
    input_img = (input_img * 2.0) - 1.0
    target_img = (target_img * 2.0) - 1.0
    return input_img, target_img

def load_dataset(input_dir, target_dir, batch_size=4):
    input_files = sorted([os.path.join(input_dir, f) for f in os.listdir(input_dir)])
    target_files = sorted([os.path.join(target_dir, f) for f in os.listdir(target_dir)])
    dataset = tf.data.Dataset.from_tensor_slices((input_files, target_files))
    dataset = dataset.map(lambda x, y: load_image(x, y), num_parallel_calls=tf.data.AUTOTUNE)
    dataset = dataset.shuffle(200).batch(batch_size).prefetch(tf.data.AUTOTUNE)
    return dataset

