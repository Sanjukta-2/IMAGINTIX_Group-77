import os
import random
import numpy as np
import tensorflow as tf
import matplotlib.pyplot as plt
from sklearn.metrics import f1_score
from skimage.color import rgb2gray

# --- Helper functions ---
def denormalize(img):
    """Convert image from [-1,1] to [0,255]"""
    img = (img + 1.0) * 127.5
    return tf.cast(tf.clip_by_value(img, 0, 255), tf.uint8)

def compute_f1(img1, img2, threshold=127):
    """
    Compute F1 score between two RGB images (convert to grayscale and threshold)
    """
    # Convert to grayscale
    gray1 = rgb2gray(img1 / 255.0)
    gray2 = rgb2gray(img2 / 255.0)
    # Threshold to binary masks
    bin1 = (gray1 * 255 > threshold).astype(np.uint8).flatten()
    bin2 = (gray2 * 255 > threshold).astype(np.uint8).flatten()
    return f1_score(bin2, bin1, zero_division=1)

def evaluate_and_show(generator, test_false_dir, test_true_dir, n=6):
    # Get list of image files
    false_imgs = sorted(os.listdir(test_false_dir))
    true_imgs = sorted(os.listdir(test_true_dir))
    assert len(false_imgs) == len(true_imgs), "False and true image counts must match!"

    # Select n random indices
    idxs = random.sample(range(len(false_imgs)), n)

    for idx in idxs:
        # Paths
        f_path = os.path.join(test_false_dir, false_imgs[idx])
        t_path = os.path.join(test_true_dir, true_imgs[idx])

        # Load and preprocess (must match your training preprocessing)
        inp = load_and_preprocess_image(f_path)       # shape (1,H,W,C), range [-1,1]
        tar = load_and_preprocess_image(t_path)       # shape (1,H,W,C), range [-1,1]

        # Generate
        pred = generator(inp, training=False)

        # Convert to displayable uint8
        inp_img = denormalize(inp[0]).numpy()
        pred_img = denormalize(pred[0]).numpy()
        tar_img = denormalize(tar[0]).numpy()

        # Metrics
        psnr_val = tf.image.psnr(pred_img, tar_img, max_val=255).numpy()
        ssim_val = tf.image.ssim(pred_img, tar_img, max_val=255).numpy()
        f1_val = compute_f1(pred_img, tar_img)

        # --- Show images ---
        plt.figure(figsize=(15,5))
        titles = [
            f"False-color Input\n{false_imgs[idx]}",
            f"Generated True-color\nPSNR={psnr_val:.2f}, SSIM={ssim_val:.3f}, F1={f1_val:.3f}",
            f"Real True-color\n{true_imgs[idx]}"
        ]
        images = [inp_img, pred_img, tar_img]

        for i in range(3):
            plt.subplot(1, 3, i+1)
            plt.imshow(images[i])
            plt.title(titles[i], fontsize=10)
            plt.axis('off')
        plt.show()

