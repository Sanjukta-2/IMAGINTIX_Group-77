def normalize_band(band):
    if np.all(np.isnan(band)):
        return np.zeros_like(band, dtype=np.uint8)
    band_min, band_max = np.nanmin(band), np.nanmax(band)
    if band_max - band_min == 0:
        return np.zeros_like(band, dtype=np.uint8)
    band_norm = (255 * (band - band_min) / (band_max - band_min))
    band_norm = np.nan_to_num(band_norm, nan=0.0)
    return band_norm.astype(np.uint8)

for fname in os.listdir(input_dir):
    if fname.endswith(".tif"):
        with rasterio.open(os.path.join(input_dir, fname)) as src:
            img = src.read()  # (C, H, W)

            if img.shape[0] == 1:
                img = normalize_band(img[0])  # (H,W)

            elif img.shape[0] >= 3:
                bands = [normalize_band(img[c]) for c in range(3)]
                img = np.stack(bands, axis=-1)  # (H,W,3)

            else:
                # If image has no usable bands → create black placeholder
                h, w = img.shape[1], img.shape[2]
                img = np.zeros((h, w, 3), dtype=np.uint8)

            out_path = os.path.join(output_dir, fname.replace(".tif", ".png"))
            Image.fromarray(img).save(out_path)

